    afcId: user.afcId,
    ccfId: user.ccfId,
  });
  password.value = '';

  imageUrl.value = user.fotoPerfil || null;
  selectedFile.value = null;

  nextTick(() => {
    window.scrollTo(0, 0);
  });
}


const onSubmit = handleSubmit(async (values) => {
  nextTick(() => {
