    confirmDialogConfirmColor.value = 'success';
    currentAction.value = 'create';
  }
  showConfirmDialog.value = true;
});

async function handleConfirmAction() {
  snackbar.value.show = false;
  try {
    if (currentAction.value === 'confirmEdit') {
      const user = usuarios.value.find(u => u.id === userToConfirmEditId.value);
      if (user) {
        executeEditUser(user);
      } else {
        showSnackbar('Error: Usuario no encontrado para editar.', 'error');
      }
    } else if (currentAction.value === 'create') {
      const userData: any = {
        nombres: nombres.value,
        apellidos: apellidos.value,
        correo: correo.value,
        celularPersonal: celularPersonal.value,
        celularCorporativo: celularCorporativo.value,
        direccion: direccion.value,
        centroCosto: centroCosto.value,
        rolId: rolId.value,
        razonSocialId: razonSocialId.value,
        sedeId: sedeId.value,
        cargoId: cargoId.value,
        estado: estado.value,
        recomendaciones: recomendaciones.value,
        epsId: epsId.value,
        arlId: arlId.value,
        afpId: afpId.value,
        afcId: afcId.value,
        ccfId: ccfId.value,
      };
      if (password.value) { userData.password = password.value; }
      const processedUser = await crearUsuario(userData);
      let successMessage = 'Usuario creado exitosamente.';
      if (selectedFile.value && processedUser.id) {
        try {
          const uploadResponse = await uploadProfilePicture(processedUser.id, selectedFile.value);
          processedUser.fotoPerfil = uploadResponse.fotoPerfilUrl;
          showSnackbar('Foto de perfil subida.', 'info');
        } catch (uploadError) {
          console.error('Error al subir la foto de perfil para el nuevo usuario:', uploadError);
          showSnackbar(`Error al subir la foto para el nuevo usuario: ${uploadError instanceof Error ? uploadError.message : String(uploadError)}`, 'warning');
        }
      }
      showSnackbar(successMessage, 'success');
      await cargarUsuarios();
      resetFormAndState();

    } else if (currentAction.value === 'update') {
      const userData: any = {
        nombres: nombres.value,
        apellidos: apellidos.value,
        correo: correo.value,
        celularPersonal: celularPersonal.value,
        celularCorporativo: celularCorporativo.value,
        direccion: direccion.value,
        centroCosto: centroCosto.value,
        rolId: rolId.value,
        razonSocialId: razonSocialId.value,
        sedeId: sedeId.value,
        cargoId: cargoId.value,
        estado: estado.value,
        recomendaciones: recomendaciones.value,
        epsId: epsId.value,
        arlId: arlId.value,
        afpId: afpId.value,
        afcId: afcId.value,
        ccfId: ccfId.value,
      };
      if (password.value) { userData.password = password.value; }

      if (selectedFile.value && editingUserId.value) {
        try {
          const uploadResponse = await uploadProfilePicture(editingUserId.value, selectedFile.value);
          userData.fotoPerfil = uploadResponse.fotoPerfilUrl;
          showSnackbar('Foto de perfil actualizada.', 'info');
        } catch (uploadError) {
          console.error('Error al subir la foto de perfil:', uploadError);
          showSnackbar(`Error al subir la foto: ${uploadError instanceof Error ? uploadError.message : String(uploadError)}`, 'warning');
        }
      } else if (!selectedFile.value && imageUrl.value === null && editingUserId.value) {
        userData.fotoPerfil = null;
      }

      await actualizarUsuario(editingUserId.value!, userData);
      showSnackbar('Usuario actualizado correctamente.', 'success');
      await cargarUsuarios();
      resetFormAndState();

    } else if (currentAction.value === 'delete') {
      if (userToDeleteId.value !== null) {
        await eliminarUsuario(userToDeleteId.value);
        showSnackbar('Usuario eliminado correctamente.', 'success');
        await cargarUsuarios();
        resetFormAndState();
      }
    } else if (currentAction.value === 'disable') {
      if (userToDisableId.value !== null) {
        await actualizarUsuario(userToDisableId.value, { estado: 'inactivo' });
        showSnackbar('Usuario deshabilitado exitosamente.', 'success');
        await cargarUsuarios();
        resetFormAndState();
      }
    }

  } catch (err: any) {
    console.error('ERROR en handleConfirmAction:', err);
    const errorMessage = err?.response?.data?.message || err?.message || 'Error desconocido al procesar la operación.';
    if (err?.response?.data?.errors) {
      const backendErrors = err.response.data.errors.map((e: any) => `${e.field}: ${e.message}`).join(', ');
      showSnackbar(`Error de validación: ${backendErrors}`, 'error');
    } else {
      showSnackbar(errorMessage, 'error');
    }
  } finally {
    showConfirmDialog.value = false;
    currentAction.value = '';
  }
}

